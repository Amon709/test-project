# Работа с файлами - Запись данных в файлы .txt


# Пример 1. Открытие и запись в файл

# Открываем файл для записи (режим 'w' перезапишет файл, если он уже существует)
with open('example.txt', 'w') as file:
    file.write('Привет, мир!\n')
    file.write('Это мой первый файл.')


# Пример 2. Добавление данных в файл

# Открываем файл в режиме добавления (режим 'a')
with open('example.txt', 'a') as file:
    file.write('\nДобавляю еще одну строку.\n')
    file.write('И еще одну строку.')


# Пример 3. Запись данных из списка

data = ['Строка 1', 'Строка 2', 'Строка 3']

# Открываем файл для записи
with open('example.txt', 'w') as file:
    for line in data:
        file.write(line + '\n')  # Добавляем перенос строки после каждой строки


# Пример 4. Использование разных кодировок

# Основные кодировки
# windows-1251 - для русского языка
# ascii - для английского языка
# utf-8 - универсальная кодировка для всех языков

# Открываем файл с указанием кодировки utf-8
with open('example_utf8.txt', 'w', encoding='utf-8') as file:
    file.write('Привет, мир!\n')
    file.write('Это файл с кодировкой UTF-8.')


# Пример 5. Практический пример — логирование в текстовый файл

def log_error(error_message):
    with open('error_log.txt', 'a', encoding='utf-8') as file:
        file.write(f'Ошибка: {error_message}\n')

# Пример использования функции логирования
log_error('Не удалось подключиться к базе данных')
log_error('Некорректный формат ввода пользователя')

