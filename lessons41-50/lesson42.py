def divide(x, y):
    """
    Делит число x на число y.

    Параметры:
    x (float): Числитель
    y (float): Знаменатель, не должен быть равен нулю

    Возвращает:
    float: Результат деления x на y

    Исключения:
    ValueError: Если делитель равен нулю
    """
    if y == 0:
        raise ValueError("Делитель не может быть равен нулю.")
    return x / y


def calculate_total(price, quantity=1, *discounts, **options):
    """
    Рассчитывает итоговую сумму с учётом количества и скидок.

    Параметры:
    price (float): Цена товара
    quantity (int, None): Количество товара, по умолчанию 1
    *discounts (float): Произвольное количество скидок
    **options: Дополнительные опции, например, 'налог' или 'доставка'

    Возвращает:
    float: Итоговая сумма с учётом скидок и опций
    """
    total = price * quantity
    for discount in discounts:
        total -= discount
    for option, value in options.items():
        if option == 'tax':
            total += total * value
        elif option == 'delivery':
            total += value
    return total
