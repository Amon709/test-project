# Типы данных

# int - целые числа (Integer)
# float - числа с плавающей точкой (Float)
# str - строки (String)
# bool - логические значения (Boolean)

age = 25  # int
height = 1.75  # float
name = "John"  #str
is_married = False  # bool
is_student = True  # bool

# Виды кавычек для записи значений строковых переменных

# 1. '
# 2. "
# 3. """
"""
asdsadasd
asdasdasd
asdasdasd
"""
# 4. '''
'''
asdasdasd
asdasdasd
asdasdasdasdsqwe
'''


# type() - определяет тип данных

print(type(age))
print(type(height))
print(type(name))
print(type(is_married))
print(type(is_student))
