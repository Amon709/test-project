# Математические операторы

# + - сложение
# - - вычитание
# * - умножение
# / - деление
# ** - возведение в степень
# % - остаток от деления
# // - целочисленное деление

number_1 = 7
number_2 = 2

print(number_1 + number_2)
print(number_1 - number_2)
print(number_1 * number_2)
print(number_1 / number_2)
print(number_1 ** number_2)
print(number_1 % number_2)
print(number_1 // number_2)

# Округление

# round(number, ndigits)
# number - число
# ndigits - количество цифр после запятой

print(round(7.74, 1))
print(round(7.75, 0))
print(round(7.75))
print(round(7, -1))
print(round(67.646846456, -2))
